package model;

public class Orders {
    private int id;
    private int quantity;

    public int getId() {
        return id;
    }

    public void setId(int orderId) {
        this.id = orderId;
    }


    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

}
